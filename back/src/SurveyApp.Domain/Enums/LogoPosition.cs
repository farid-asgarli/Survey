namespace SurveyApp.Domain.Enums;

/// <summary>
/// Represents the position of the logo in a survey theme.
/// </summary>
public enum LogoPosition
{
    /// <summary>
    /// Logo positioned at top left.
    /// </summary>
    TopLeft = 0,

    /// <summary>
    /// Logo positioned at top center.
    /// </summary>
    TopCenter = 1,

    /// <summary>
    /// Logo positioned at top right.
    /// </summary>
    TopRight = 2,

    /// <summary>
    /// Logo positioned at bottom left.
    /// </summary>
    BottomLeft = 3,

    /// <summary>
    /// Logo positioned at bottom center.
    /// </summary>
    BottomCenter = 4,

    /// <summary>
    /// Logo positioned at bottom right.
    /// </summary>
    BottomRight = 5,
}
