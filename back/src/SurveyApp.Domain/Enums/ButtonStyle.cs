namespace SurveyApp.Domain.Enums;

/// <summary>
/// Represents the style of buttons in a survey theme.
/// </summary>
public enum ButtonStyle
{
    /// <summary>
    /// Rounded corners.
    /// </summary>
    Rounded = 0,

    /// <summary>
    /// Square corners.
    /// </summary>
    Square = 1,

    /// <summary>
    /// Fully rounded (pill shape).
    /// </summary>
    Pill = 2,
}
